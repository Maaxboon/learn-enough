// Numbers: Numbers in JavaScript can be integers or floating-point values. Integers are whole numbers like 1, 2, or 3, and floating-point values are numbers with decimal points like 7.8 or 0.05.

// typeof() is a method for used to find out the type of any value we create in JavaScript

// You can recognize methods with the parenthesis() after them. The represent a set of instructions for the computer.

// Whole Numbers/Integers: 2 , 22, 222, 2222, -22

console.log(typeof 2); // number
console.log(typeof -22); // number

// Floating point numbers: 0.4, 4.0, 40.4

console.log(typeof 0.4); // number

// Strings: A string is a group of characters that is used to store text. Can be a single word, sentence or multiple paragraphs and can contain any combination of letters, numbers and spaces.

console.log("My name in Maxwel Okoth");

console.log(typeof "Hello Maxie");

// Booleans: Booleans represent true or false values. They are often used to make comparisons and to determine whether a set of instructions should be executed.

// Comparisons: > (greater than), < (less than), === (is the same as)

console.log("hey" == "hey"); // true
console.log("hey" === "hey"); // true
console.log("hey" === "hEy"); // false: strings are case sensitive

